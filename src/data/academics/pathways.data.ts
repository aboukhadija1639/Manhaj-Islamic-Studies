/**
 * الحصول على المسارات الممكنة من تخصص ليسانس معين
 */
export function getPathwaysFromLicence(licenceSpecialtyId: string): AcademicPathway[] {
  return academicPathways.filter(
    (pathway) => pathway.licenceSpecialtyId === licenceSpecialtyId
  );
}

/**
 * الحصول على المسارات المؤدية إلى تخصص ماستر معين
 */
export function getPathwaysToMaster(masterSpecialtyId: string): AcademicPathway[] {
  return academicPathways.filter((pathway) =>
    pathway.masterSpecialtyIds.includes(masterSpecialtyId)
  );
}
